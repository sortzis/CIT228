fruits = ["oranges","peaches","pineapple","lime","lemon"]
for fruit in fruits:
    print("The fruit is: ",fruit)
    if fruit=="oranges":
        print("They are orange in color")
    if fruit=="peaches":
        print("Those are my favorite!")
    if fruit=="pineapple":
        print("They grow from the crown down")
    if fruit=="lime":
        print("They are great as seasoning on tacos and guacamole")
    if fruit=="lemon":
        print("Lemonade would be nothing without them.")